package com.biz.rev;

public class Var_05 {

	public static void main(String[] args) {

		int num = 30;
		
		/*
		 * 앞에서 선언되고 30이 저장된 num변수값을 읽어서
		 * 그 값에 40을 더하고 결과인 70을
		 * num변수에 다시 저장하라
		 */
		num = num + 40; // 30+40이 num에 저장
		num += 40; // 축약형 코드 70+40이 num에 저장
		
		// 앞에서 선언되고 값이 저장된 num변수 값을 읽어서
		// 그 값에 1을 더하고 그 결과인 111을 다시 num값에 저장하라
		// == num변수 값을 1 증가시켜라
		num++ ; 
		
		// 앞에서 선언되고 값이 저장된 num변수 값을 읽어서
		// 그 값에 1을 빼고 그 결과인 110을 다시 num값에 저장하라
		// == num 변수 값을 1 감소시켜라
		num--;
		
		
		
		
		
	}

}
