package com.biz.rev;

public class Var_04 {

	public static void main(String[] args) {

		// 정수형 변수 num를 선언한 후, 
		// 3*4 연산을 수행한 후
		// 그 결과를 변수 num에 보관하라
		int num = 3 * 4;
		
		// 앞에서 선언된 변수 num에
		// 4/2 연산을 수행한 후
		// 그 결과를 변수 num에 보관하라
		// 만약 num가 앞에서 선언되지 않으면 오류가 발생한다.
		num = 4 / 2;
		
		String s = "KOREA";
		
		// 앞에서 선언된 변수 s에
		// 3가지 문자열을 덧셈한 후(연결한 후) 저장하라
		s = "Republic" + "of" + "Korea";
		// s에 저장된 값?
		// RepublicofKorea
		
		// 앞에서 선언된 변수 s에
		// 문자열 "30+40"을 저장하라
		s = "30 + 40";
		//s에 저장된 값은??
		// 30 + 40  ==> 정수형이 아니고 문자열형이기때문에 
		//				따로 연산안하고 문자 그대로 저장함
		
				
		
	}

}
