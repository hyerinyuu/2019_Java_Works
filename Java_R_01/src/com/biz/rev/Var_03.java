package com.biz.rev;

public class Var_03 {

	public static void main(String[] args) {

		//Var = Variable = 변수
		
		// 문자열형 변수 s를 선언하고
		// 문자열 KOREA 를 보관하라
		String s = "KOREA";		
		
		// String이 붙은것과 안 붙은것의 차이
		// 앞에서 선언된 변수 s에
		// 문자열 "대한"과 "민국"을 덧셈하여(=연결하라)
		// 변수 s에 보관하라
		s = "대한" + "민국";
		
		// 변수 s에 보관중인 값을 console에 보여라
		System.out.println(s);
		
	}

}
