package com.biz.forr;

public class For_01 {

	public static void main(String[] args) {

		// 어떤값이 0 ~ (10-1) 까지 1씩 증가하는동안
		// {}안에 있는 코드를 반복 실행하라
		// i<10이 될때까지
		for(int i = 0 ; i <10 ; i++) {
			
		}
		
		int num = 100;
		// 앞에서 선언되고 값이 저장된 num 변수를 참조하여
		// 어떤값이 0 ~ (num -1 )까지 1씩 증가하는 동안
		// {}안에 있는 코드를 반복 실행하라
		// i < num이 될때까지
		for(int i = 0 ; i < num ; i++) {
			
		}
		
		// 변수 이름은 상관 없음 (index, n)
		for(int index = 0 ; index < num ; index++) {
			
		}
		for(int n = 0 ; n < num ; n++) {
			
		}
		// 변수 x값이 0부터 num-1까지 1씩 증가하고 있는 동안
		// 변수 x의 변화되는 값을 console에 보여라
		for(int x = 0 ; x < num ; x++) {
			System.out.println(x);
		}
		
	}

}
