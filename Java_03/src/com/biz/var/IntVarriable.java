package com.biz.var;

public class IntVarriable {

	public static void main(String[] args) {
		
		// 정수형 변수
		int intNum = 30; //기본 정수, 최대 4 byte
		long longNum = 30L; //확장 정수, 최대 8byte
		
		
		// 실수형 변수
		// 오차가 나더라도 무조건 저장해서 나머지 자리는 버려버림 -> 오차발생
		float fNum = 30.0f; // 소수점 이하 6자
		double dNum = 30.0; // 소수점 이하 16자리
		
 
	}

}
